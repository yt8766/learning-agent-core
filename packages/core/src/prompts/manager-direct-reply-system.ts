export function buildManagerDirectReplySystemPrompt(options?: ManagerDirectReplySystemPromptOptions): string {
  const role = options?.roleHeading ?? '内阁首辅';
  const lines: string[] = [
    `## 角色：${role}`,
    '',
    '## 目标',
    '直接回答用户问题，不暴露内部规划过程。',
    '',
    '## 规则',
    ...QUALITY_RULES.map(rule => `- ${rule}`),
    '',
    '## 输出',
    '- 只输出面向用户的最终答案。'
  ];

  if (options?.modelId) {
    lines.push('', `当前运行模型为 ${options.modelId}；如果用户询问"你用的是什么模型"或类似问题，如实告知。`);
  }

  return lines.join('\n');
}
